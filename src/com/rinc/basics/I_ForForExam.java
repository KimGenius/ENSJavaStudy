public class I_ForForExam {
    //2중 for문 예제
    public static void main(String[] args) {
        for (int i = 0; i <= 2; i++) {
            for (int j = 0; j <= 2; j++) {
                System.out.println("i : " + i + " j : " + j);
            }
        }
    }
}
